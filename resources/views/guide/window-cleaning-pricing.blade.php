<h2>支払い方法と契約条件</h2>

<h3>一般的な支払い条件</h3>
<ul>
    <li><strong>戸建て住宅</strong>：作業完了後の現金支払いが一般的</li>
    <li><strong>法人契約</strong>：月末締め翌月末払いの請求書払い</li>
    <li><strong>定期契約</strong>：年払い・半年払いで割引適用の場合もあり</li>
</ul>

<h3>キャンセル料</h3>
<p>天候等による作業中止の場合はキャンセル料は発生しませんが、依頼者都合でのキャンセルには料金が発生することがあります：</p>

<ul>
    <li><strong>前日キャンセル</strong>：料金の30〜50%</li>
    <li><strong>当日キャンセル</strong>：料金の50〜100%</li>
</ul>

<p>窓ガラス清掃の料金は、建物の規模や作業内容によって大きく変わりますが、適正な価格で信頼できる業者を選ぶことで、美しい仕上がりと安全な作業を確保することができます。複数の見積もりを比較し、価格だけでなく技術力やサービス内容も総合的に判断することが重要です。</p>

