<h2>作業内容による料金の違い</h2>

<h3>基本清掃</h3>
<p>一般的な窓ガラス清掃では、ガラス面の水洗いとスクイジーを使った水切り作業が基本となります。</p>

<ul>
    <li><strong>内側のみ清掃</strong>：料金の50〜60%程度</li>
    <li><strong>外側のみ清掃</strong>：料金の70〜80%程度（高所作業リスク込み）</li>
    <li><strong>内外両面清掃</strong>：標準料金（100%）</li>
</ul>

<h3>追加サービス</h3>
<div class="bg-amber-50 border border-amber-200 rounded-lg p-4 my-6">
    <h4 class="text-amber-800 font-semibold mb-2">オプション清掃サービス</h4>
    <ul class="text-amber-700 space-y-1">
        <li>• <strong>窓枠・サッシ清掃</strong>：基本料金の20〜30%追加</li>
        <li>• <strong>網戸清掃</strong>：1枚あたり300〜800円</li>
        <li>• <strong>雨樋清掃</strong>：5,000〜15,000円</li>
        <li>• <strong>ガラスコーティング</strong>：基本料金の50〜80%追加</li>
        <li>• <strong>緊急対応</strong>：基本料金の20〜50%増し</li>
    </ul>
</div>

