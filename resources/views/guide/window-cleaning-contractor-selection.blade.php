<h2>契約前の最終確認事項</h2>

<h3>契約書の内容確認</h3>
<p>正式契約前に以下の項目を必ず確認します：</p>

<ul>
    <li><strong>作業範囲の明記</strong>：清掃対象と除外事項の明確化</li>
    <li><strong>料金と支払い条件</strong>：総額、支払い方法、期日</li>
    <li><strong>作業日程</strong>：開始日、完了予定日、雨天時の対応</li>
    <li><strong>保険と責任範囲</strong>：損害発生時の対応</li>
    <li><strong>変更・キャンセル条件</strong>：変更時の手続きと料金</li>
</ul>

