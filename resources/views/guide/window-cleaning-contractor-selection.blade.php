    <li><strong>特殊案件の対応経験</strong>：湾曲ガラス、特殊形状建物等</li>
    <li><strong>業界での経験年数</strong>：個人と会社両方の経験</li>
    <li><strong>顧客継続率</strong>：定期契約の更新率</li>
</ul>

